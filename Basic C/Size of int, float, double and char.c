#include <stdio.h>

int main() {
    int i;
    float f;
    double d;
    char c;

    printf("Size of int number %d\n", sizeof(i));
    printf("Size of float number %d\n", sizeof(f));
    printf("Size of double number %d\n", sizeof(d));
    printf("Size of char number %d\n", sizeof(c));

    return 0;
}
